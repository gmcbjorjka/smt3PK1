    private void simpan_data() {
        bt_tambah.setText("TAMBAH");
        String id_kur = txt_id_kurir.getText();
        String nama = txt_nama_kurir.getText();
        String kontak = txt_no_kontak.getText();
        String alamat = txt_alamat.getText();
        
        if (id_kur.isEmpty()) {
            id_kur = generateID();
            txt_id_kurir.setText(id_kur);
        }
        
        String q = "INSERT INTO "
                + "kurir(id_kurir,nama_kurir,no_kontak,alamat) "
                + "VALUES ('"+id_kur+"','"+nama+"','"+kontak+"','"+alamat+"')";
        Connection c = koneksi.konek();
        try {
            Statement s = c.createStatement();
            int result = s.executeUpdate(q);
            if (result > 0) {
            // Jika simpan berhasil
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan", "Sukses", JOptionPane.INFORMATION_MESSAGE);

            // Kembali ke panel data_destinasi
            Mainpanel.removeAll();
            Mainpanel.repaint();
            Mainpanel.revalidate();

            Mainpanel.add(data_kurir);
            Mainpanel.repaint();
            Mainpanel.revalidate();

            // Refresh tabel
            refreshtable();
            resetForm();
        } else {
            // Jika simpan gagal
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data", "Error", JOptionPane.ERROR_MESSAGE);
        }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

